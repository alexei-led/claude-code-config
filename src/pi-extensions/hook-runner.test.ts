type ExecCallback = (err: Error | null, stdout: string, stderr: string) => void;

const execQueue: Array<{ exitCode: number; stdout: string; stderr: string }> = [];
const capturedCommands: string[] = [];
const settingsFiles = new Map<string, string>();
const commands = new Map<string, { handler: (args: string, ctx: ReturnType<typeof makeCtx>) => Promise<void> }>();
// The bundled hooks.json is now generated by compile_hook from src/hooks/*/meta.yaml
// plus src/pi-extensions/hooks-external.json. The test reads it from dist/ to keep
// fixtures honest about the real merged shape — run `make build` first.
const bundledHooksConfigPath = `${process.cwd()}/src/pi-extensions/hooks.json`;
const generatedHooksConfigPath = `${process.cwd()}/dist/pi/extensions/hooks.json`;
const bundledHooksConfig = await Bun.file(generatedHooksConfigPath).text();
const originalPiCodingAgentDir = process.env.PI_CODING_AGENT_DIR;
let capturedStdin = "";

mock.module("node:child_process", () => ({
	execFile: (_cmd: string, args: string[], _opts: unknown, cb: ExecCallback) => {
		capturedCommands.push(args.join(" "));
		const resp = execQueue.shift() ?? { exitCode: 0, stdout: "", stderr: "" };
		const child = {
			stdin: {
				write: (data: string) => {
					capturedStdin = data;
				},
				end: () => {},
			},
		};
		setTimeout(() => {
			if (resp.exitCode === 0) {
				cb(null, resp.stdout, resp.stderr);
			} else {
				const err = Object.assign(new Error("exit"), { code: resp.exitCode });
				cb(err as Error, resp.stdout, resp.stderr);
			}
		}, 0);
		return child;
	},
}));

mock.module("node:fs", () => ({
	readFileSync: (path: string) => {
		const content = settingsFiles.get(path);
		if (typeof content === "string") return content;
		throw Object.assign(new Error("ENOENT"), { code: "ENOENT" });
	},
	writeFileSync: (path: string, data: string) => {
		settingsFiles.set(path, data);
	},
	mkdirSync: () => undefined,
	existsSync: () => false,
	readdirSync: () => [],
	statSync: () => ({ isDirectory: () => false }),
	rmSync: () => undefined,
	mkdtempSync: (prefix: string) => `${prefix}mock`,
	appendFileSync: () => undefined,
}));

mock.module("node:os", () => ({ homedir: () => "/tmp/test-home" }));
mock.module("@earendil-works/pi-coding-agent", () => ({}));

