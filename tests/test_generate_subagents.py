def test_rejects_mcp_agent_without_pi_override(tmp_path):
    _write_agent(
        tmp_path,
        "demo",
        "researcher.md",
        "---\n"
        "name: researcher\n"
        "description: Research\n"
        "tools: [Read, mcp__context7__query-docs]\n"
        "---\n"
        "Use mcp__context7__query-docs.\n",
    )

    with patch.object(generate_subagents, "ROOT", tmp_path):
        state = generate_subagents.compute_desired_state()

    out = tmp_path / "plugins" / "demo" / "agents-pi" / "researcher.md"
    assert out not in state.files
    assert len(state.rejected) == 1
    assert "requires unavailable tools" in state.rejected[0].reasons[0]
    assert "mentions Claude-only or unavailable tools" in state.rejected[0].reasons[1]


def test_duplicate_flat_agent_filename_fails(tmp_path):
    for plugin in ("one", "two"):
        _write_agent(
            tmp_path,
            plugin,
            "nested/worker.md",
            "---\ndescription: Worker\ntools: [Read]\n---\nBody.\n",
        )

    with patch.object(generate_subagents, "ROOT", tmp_path):
        with pytest.raises(
            generate_subagents.AgentGenerationError,
            match="duplicate Pi agent filename worker.md",
        ):
            generate_subagents.compute_desired_state()


def test_invalid_frontmatter_fields_are_not_exported(tmp_path):
    _write_agent(
        tmp_path,
        "demo",
        "worker.md",
        "---\n"
        "name: worker\n"
        "description: Worker\n"
        "tools: [Read, Write]\n"
        "color: blue\n"
        "effort: medium\n"
        "prompt_mode: replace\n"
        "memory: project\n"
        "enabled: true\n"
        "---\n"
        "Body.\n",
    )

    with patch.object(generate_subagents, "ROOT", tmp_path):
        state = generate_subagents.compute_desired_state()

    out = tmp_path / "plugins" / "demo" / "agents-pi" / "worker.md"
    metadata = frontmatter.loads(state.files[out].data.decode()).metadata
    assert set(metadata) <= set(generate_subagents.PI_AGENT_FRONTMATTER_FIELDS)
    assert metadata["tools"] == "read, write"
    assert metadata["memory"] == "project"
    assert metadata["thinking"] == "medium"
    assert metadata["prompt_mode"] == "replace"
    assert metadata["enabled"] is True
    assert "name" not in metadata
    assert "color" not in metadata


def test_sync_writes_flat_files_and_removes_nested_stale_outputs(tmp_path):
    _write_agent(
        tmp_path,
        "demo",
        "nested/worker.md",
        "---\ndescription: Worker\ntools: [Read]\n---\nBody.\n",
    )
    stale = tmp_path / "plugins" / "demo" / "agents-pi" / "nested" / "old.md"
    stale.parent.mkdir(parents=True)
    stale.write_text("stale\n")

    with patch.object(generate_subagents, "ROOT", tmp_path):
        state = generate_subagents.compute_desired_state()
        changes = generate_subagents.sync(state.files)

    out = tmp_path / "plugins" / "demo" / "agents-pi" / "worker.md"
    assert changes > 0
    assert out.is_file()
    assert not stale.exists()
    assert not stale.parent.exists()
