from __future__ import annotations

import importlib.util
import sys
from pathlib import Path
from unittest.mock import patch

import frontmatter
import pytest

_spec = importlib.util.spec_from_file_location(
    "generate_subagents",
    Path(__file__).resolve().parent.parent
    / "scripts"
    / "build"
    / "generate-subagents.py",
)
assert _spec is not None and _spec.loader is not None
generate_subagents = importlib.util.module_from_spec(_spec)
sys.modules[_spec.name] = generate_subagents
_spec.loader.exec_module(generate_subagents)


def _write_agent(root: Path, plugin: str, rel_path: str, body: str) -> Path:
    path = root / "plugins" / plugin / "agents" / rel_path
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(body)
    return path


def test_converts_claude_agent_frontmatter_to_pi(tmp_path):
    _write_agent(
        tmp_path,
        "demo",
        "py/py-impl.md",
        "---\n"
        "name: py-impl\n"
        "description: Python implementation reviewer\n"
        "tools:\n"
        "  - Read\n"
        "  - Bash(uv *)\n"
        "  - Grep\n"
        "  - Glob\n"
        "  - LS\n"
        "model: sonnet\n"
        "effort: high\n"
        "color: yellow\n"
        "skills: [writing-python, smart-explore]\n"
        "---\n"
        "Review Python code.\n",
    )

    with patch.object(generate_subagents, "ROOT", tmp_path):
        state = generate_subagents.compute_desired_state()

    out = tmp_path / "plugins" / "demo" / "agents-pi" / "py-impl.md"
    post = frontmatter.loads(state.files[out].data.decode())
    assert state.rejected == ()
    assert post.metadata == {
        "description": "Python implementation reviewer",
        "tools": "read, bash, grep, find, ls",
        "skills": "writing-python, smart-explore",
        "disallowed_tools": "edit, write",
        "model": "sonnet",
        "thinking": "high",
    }
    assert post.content.endswith("Review Python code.")
    assert "generated by scripts/build/generate-subagents.py" in post.content


