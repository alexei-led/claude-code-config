#!/usr/bin/env bash
# file-protector.sh - PreToolUse hook to prevent writes to sensitive files
#
# EXIT CODES
#   0 - Allow the operation
#   2 - Block with message (sent to Claude via stderr)

set -euo pipefail

INPUT=$(cat)
FILE_PATH=$(echo "$INPUT" | jq -r '.tool_input.file_path // .tool_input.path // ""' 2>/dev/null || echo "")

# Exit early if no file path
[[ -z "$FILE_PATH" ]] && exit 0

# Protected file patterns (regex)
PROTECTED_PATTERNS=(
	'\.env$'
	'\.env\.'
	'/\.env$'
	'secrets/'
	'secret[s]?\.'
	'\.key$'
	'\.pem$'
	'\.p12$'
	'\.pfx$'
	'\.ssh/'
	'id_rsa'
	'id_ed25519'
	'credentials'
	'password'
	'\.secret$'
	'api[_-]?key'
	'auth[_-]?token'
)

# Lock files - warn but allow with confirmation context
LOCK_FILES=(
	'package-lock\.json$'
	'yarn\.lock$'
	'pnpm-lock\.yaml$'
	'go\.sum$'
	'Cargo\.lock$'
	'poetry\.lock$'
	'Gemfile\.lock$'
	'composer\.lock$'
)

# Check protected patterns - BLOCK
for pattern in "${PROTECTED_PATTERNS[@]}"; do
	if [[ "$FILE_PATH" =~ $pattern ]]; then
		echo "BLOCKED: Cannot modify sensitive file: $FILE_PATH" >&2
		echo "Pattern matched: $pattern" >&2
		echo "If intentional, temporarily disable this hook in settings.json" >&2
		exit 2
	fi
done

# Check lock files - WARN but allow
for pattern in "${LOCK_FILES[@]}"; do
	if [[ "$FILE_PATH" =~ $pattern ]]; then
		echo "WARNING: Modifying lock file: $FILE_PATH" >&2
		echo "Lock files should typically be auto-generated by package managers" >&2
		exit 0
	fi
done

exit 0
